export const featuredArtworks = artworks.filter((a) => a.featured);

export function getArtwork(id: string): Artwork | undefined {
  return artworks.find((a) => a.id === id);
}

export function getAdjacentArtworks(id: string): { prev: Artwork | null; next: Artwork | null } {
  const index = artworks.findIndex((a) => a.id === id);
  return {
    prev: index > 0 ? artworks[index - 1] : null,
    next: index < artworks.length - 1 ? artworks[index + 1] : null,
  };
}
